            In January 2000, FSRI hired Jeffrey Brusini to create a day treatment facility for children with emotional and behavioral challenges.  The facility was to be based in Providence.  In the ten years prior, Mr. Brusini had been the director of a small private school for children with similar needs in Warwick, RI. 
          </p>
